num = int(input("Enter a number -> "))
if (num >= 90):
  print("Input is 90 or more")
  print("A")
elif (num >= 80):
  print("Input is 80 or more")
  print("B")
elif (num >= 70):
  print("Input is 70 or more")
  print("C")
elif (num >= 60):
  print("Input is 60 or more")
  print("D")
else:
  print("Input is less than 60")
